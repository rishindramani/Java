package nestedpatterns;

public class nested12alphabet//q2//
{
    public void main()
    {
         int i,j;
         for(i=69;i>=65;i--)
         {
             for(j=69;j>=i;j--)
             {
                 System.out.print((char)j);
                }
                System.out.println();
            }
        }
    }
                