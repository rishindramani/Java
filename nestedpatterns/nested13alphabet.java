package nestedpatterns;

public class nested13alphabet//q3//
{
    public void main()
    {
         int i,j;
         for(i=69;i>=65;i--)
         {
             for(j=i;j<=69;j++)
             {
                 System.out.print((char)j);
                }
                System.out.println();
            }
        }
    }
                