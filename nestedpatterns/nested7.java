package nestedpatterns;

public class nested7//q7//
{
    public static void main()
    {
        int i,j;
        for(i=5;i>=1;i--)
            {
                for(j=5;j>=i;j--)
            {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
           
                  