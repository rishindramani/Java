package nestedpatterns;

public class nested10alphabet//eg1//
{
    public static void main()
    {
         int i,j;
         for(i=65;i<=69;i++)
         {
             for(j=65;j<=i;j++)
             {
                 System.out.print((char)j);
                }
                System.out.println();
            }
        }
    }
                