package nestedpatterns;

public class nested17alphabet//q7//
{
    public static void main()
    {
         int i,j;
         for(i=69;i>=65;i--)
         {
             for(j=i;j>=65;j--)
             {
                 System.out.print((char)j);
                }
                System.out.println();
            }
        }
    }
                