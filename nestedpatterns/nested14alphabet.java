package nestedpatterns;

public class nested14alphabet//q4//
{
    public void main()
    {
         int i,j;
         for(i=69;i>=65;i--)
         {
             for(j=65;j<=i;j++)
             {
                 System.out.print((char)j);
                }
                System.out.println();
            }
        }
    }
                