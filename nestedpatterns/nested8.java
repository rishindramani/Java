package nestedpatterns;

public class nested8//q6//
{
    public static void main()
    {
        int i,j;
        for(i=5;i>=1;i--)
            {
                for(j=i;j>=1;j--)//or for(j=1;j<=i;j++)
            {
                System.out.print(i);
            }
            System.out.println();
        }
    }
}
           
                  