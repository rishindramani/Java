package nestedpatterns;

public class nested2//Q4//
{
    public static void main()
    {
        int i,j;
        for(i=5;i>=1;i--)
            {
                for(j=i;j<=5;j++)
            {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
           
                  