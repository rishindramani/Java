package nestedpatterns;

public class nested11alphabet//q1//
{
    public void main()
    {
         int i,j;
         for(i=65;i<=69;i++)
         {
             for(j=i;j>=65;j--)
             {
                 System.out.print((char)j);
                }
                System.out.println();
            }
        }
    }
                