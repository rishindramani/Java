package nestedpatterns;

public class nested3//Q2//
{
    public static void main()
    {
        int i,j;
        for(i=5;i>=1;i--)
            {
                for(j=1;j<=i;j++)
            {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
           
                  