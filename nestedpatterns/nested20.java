package nestedpatterns;

public class nested20
{
    public static void main()
    {
        int i,j,c=0;
        for(i=1;i<=5;i++)
            {
                for(j=1;j<=i;j++)
                {
                    c++;
                System.out.print(c);
            }
            System.out.println();
        }
    }
}
           
                  