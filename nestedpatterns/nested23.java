package nestedpatterns;

public class nested23
{
    public static void main()
    {
        int i,j;
        for(i=1;i<=4;i++)
            {
                for(j=1;j<=i;j++)
                {
                    if(j%2==0)
                { System.out.print("0");
                    
            }
            else
            {
           System.out.print("1");
        }}
    System.out.println();
}
}
}           
                  