package nestedpatterns;

public class nested6//q1//
{
    public static void main()
    {
        int i,j;
        for(i=1;i<=5;i++)
            {
                for(j=i;j>=1;j--)
            {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
           
                  