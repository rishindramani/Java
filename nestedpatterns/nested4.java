package nestedpatterns;

public class nested4//q1//
{
    public static void main()
    {
        int i,j;
        for(i=1;i<=5;i++)
            {
                for(j=i;j<=5;j++)
            {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
           
                  