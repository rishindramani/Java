package nestedpatterns;

public class nested25
{
    public static void main()
    {
        int i,j;
        for(i=1;i<=9;i+=2)
            {
                for(j=i;j>=1;j-=2)
            {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
           
                  